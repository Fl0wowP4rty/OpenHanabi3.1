   private HFontRenderer getFont(String name, int size) {
      Font font;
      try {
         InputStream is = this.getClass().getResourceAsStream("/assets/minecraft/Client/fonts/" + name + ".ttf");
         font = Font.createFont(0, is);
         font = font.deriveFont(0, (float)size);
      } catch (Exception var5) {
         var5.printStackTrace();
         System.out.println("Error loading font");
         font = new Font("default", 0, size);
      }

      return new HFontRenderer(font, size, true);
   }

